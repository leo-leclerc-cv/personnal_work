document.getElementById("downloadTop").addEventListener("mouseenter", function( event ) {
	document.getElementById("downloadTop").style.paddingTop="30px";
	document.getElementById("downloadTop").style.paddingBottom="0px";
	document.getElementById("downloadBottom").style.paddingTop="0px";
	document.getElementById("downloadBottom").style.paddingBottom="30px";
}, false);

document.getElementById("downloadTop").addEventListener("mouseleave", function( event ) {
	document.getElementById("downloadTop").style.paddingTop="15px";
	document.getElementById("downloadTop").style.paddingBottom="15px";
	document.getElementById("downloadBottom").style.paddingTop="15px";
	document.getElementById("downloadBottom").style.paddingBottom="15px";
}, false);

